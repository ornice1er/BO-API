<?php

namespace App\Http\Controllers;

use App\Http\Repositories\PersonalRepository;
use App\Http\Requests\Personal\StorePersonalRequest;
use App\Http\Requests\Personal\UpdatePersonalRequest;
use App\Services\LogService;
use App\Utilities\Common;
use Illuminate\Http\Request;
use OpenApi\Attributes as OA;

class PersonalController extends Controller
{
    /**
     * The personal repository being queried.
     *
     * @var PersonalRepository
     */
    protected $personalRepository;

    protected $ls;

    public function __construct(PersonalRepository $personalRepository, LogService $ls)
    {
        $this->personalRepository = $personalRepository;
        $this->ls = $ls;

        //$this->middleware('auth:api')->except(['getNotified', 'show']);

    }

    /** @OA\Get(
     *      path="/personals",
     *      operationId="Personal list",
     *      tags={"Personal"},
     *       security={{"JWT":{}}},
     *      summary="Return personal data",
     *      description="Get all personals",
     *
     *      @OA\Parameter(
     *          name="name",
     *          in="query",
     *          description="Can be used for filtering data by name",
     *          required=false,
     *
     *          @OA\Schema(
     *              type="string"
     *          )
     *      ),
     *
     * @OA\Parameter(
     *          name="project_id",
     *          in="query",
     *          description="Project ID",
     *
     *          @OA\Schema(
     *              type="integer"
     *          )
     *      ),
     *
     * @OA\Parameter(
     *          name="role",
     *          in="query",
     *          description="Personal Role ID",
     *          required=false,
     *
     *          @OA\Schema(
     *              type="string"
     *          )
     *      ),
     *
     *      @OA\Parameter(
     *          name="categorie",
     *          in="query",
