     *      path="/startPoints-search",
     *      operationId="StartPoint searching",
     *      tags={"StartPoint"},
     *       security={{"JWT":{}}},
     *      summary="Return list of StartPoint respecting term",
     *      description="Get all filtered startPoints using term",
     *
     *     @OA\Response(
