     *
     *      @OA\Parameter(
     *          name="id",
     *          in="path",
     *          description="StartPoint ID",
     *          required=true,
     *
     *          @OA\Schema(
     *              type="string"
     *          )
     *      ),
     *
     *      @OA\Parameter(
     *          name="state",
     *          in="path",
     *          description="StartPoint state",
     *          required=true,
     *
     *          @OA\Schema(
     *              type="string"
     *          )
     *      ),
     *      summary="Change StartPoint state",
     *      description="Change StartPoint state by ID",
     *
     *      @OA\Response(
     *          response=200,
     *          description="Successful operation",
     *
     *          @OA\JsonContent(ref="#/components/schemas/StartPoint"),
     *
     *          @OA\XmlContent(ref="#/components/schemas/StartPoint")
