<?php

namespace App\Http\Controllers;

use App\Http\Repositories\ProfilRepository;
use App\Http\Requests\Profil\StoreProfilRequest;
use App\Http\Requests\Profil\UpdateProfilRequest;
use App\Services\LogService;
use App\Utilities\Common;
use Illuminate\Http\Request;
use OpenApi\Attributes as OA;

class ProfilController extends Controller
{
    /**
     * The profil repository being queried.
     *
     * @var ProfilRepository
     */
    protected $profilRepository;

    protected $ls;

    public function __construct(ProfilRepository $profilRepository, LogService $ls)
    {
        $this->profilRepository = $profilRepository;
        $this->ls = $ls;

        //$this->middleware('auth:api')->except(['getNotified', 'show']);

    }

    /** @OA\Get(
     *      path="/profils",
     *      operationId="Profil list",
     *      tags={"Profil"},
     *       security={{"JWT":{}}},
     *      summary="Return profil data",
     *      description="Get all profils",
     *
     *      @OA\Parameter(
     *          name="name",
     *          in="query",
     *          description="Can be used for filtering data by name",
     *          required=false,
     *
     *          @OA\Schema(
     *              type="string"
     *          )
     *      ),
     *
     * @OA\Parameter(
     *          name="project_id",
     *          in="query",
     *          description="Project ID",
     *
     *          @OA\Schema(
     *              type="integer"
     *          )
     *      ),
     *
     * @OA\Parameter(
     *          name="role",
     *          in="query",
     *          description="Profil Role ID",
     *          required=false,
     *
     *          @OA\Schema(
     *              type="string"
     *          )
     *      ),
     *
     *      @OA\Parameter(
     *          name="categorie",
     *          in="query",
     *          description="Can be used for filtering data by categorie| ANIMATRICE,RESPONSABLE",
     *          required=false,
     *
     *          @OA\Schema(
     *              type="string"
     *          )
     *          ),
     *
     *      @OA\Response(
     *          response=200,
     *          description="Successful operation",
     *
