     *      ),
     *      @OA\Response(
     *          response=404,
     *          description="Not found"
     *      ),
     *      @OA\Response(
     *          response=500,
     *          description="Server Error"
     *      )
     * )
     */
    public function changeState($id, $state)
    {
        $message = 'Changement de l\'état d\'un prestation';

        try {
            $result = $this->prestationRepository->setStatus($id, $state);
            $statusMessage = $state == 1 ? 'activé' : 'désactivé';
            $this->ls->trace(['action_name' => $message, 'description' => json_encode($result)]);

            return Common::success("Utilisateur $statusMessage avec succès", $result);
        } catch (\Throwable $th) {
            $this->ls->trace(['action_name' => $message, 'description' => $th->getMessage()]);

            return Common::error($th->getMessage(), []);
        }
    }

    /** @OA\Post(
     *      path="/prestations-search",
     *      operationId="Prestation searching",
     *      tags={"Prestation"},
     *       security={{"JWT":{}}},
     *      summary="Return list of Prestation respecting term",
     *      description="Get all filtered prestations using term",
     *
     *     @OA\Response(
     *         response=200,
     *         description="Successful operation",
     *
     *         @OA\JsonContent(ref="#/components/schemas/Prestation"),
     *
     *         @OA\XmlContent(ref="#/components/schemas/Prestation")
     *     ),
     *
     *     @OA\RequestBody(
