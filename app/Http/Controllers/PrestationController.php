     *
     *      @OA\Parameter(
     *          name="id",
     *          in="path",
     *          description="Prestation ID",
     *          required=true,
     *
     *          @OA\Schema(
     *              type="string"
     *          )
     *      ),
     *
     *      @OA\Parameter(
     *          name="state",
     *          in="path",
     *          description="Prestation state",
     *          required=true,
     *
     *          @OA\Schema(
     *              type="string"
     *          )
     *      ),
     *      summary="Change Prestation state",
     *      description="Change Prestation state by ID",
     *
     *      @OA\Response(
     *          response=200,
     *          description="Successful operation",
     *
     *          @OA\JsonContent(ref="#/components/schemas/Prestation"),
     *
     *          @OA\XmlContent(ref="#/components/schemas/Prestation")
     *      ),
     *
     *      @OA\Response(
     *          response=400,
