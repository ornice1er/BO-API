<?php

namespace App\Http\Controllers;

use App\Http\Repositories\FilesRepository;
use App\Http\Requests\Files\StoreFilesRequest;
use App\Http\Requests\Files\UpdateFilesRequest;
use App\Services\LogService;
use App\Utilities\Common;
use Illuminate\Http\Request;
use OpenApi\Attributes as OA;

class FilesController extends Controller
{
    /**
     * The files repository being queried.
     *
     * @var FilesRepository
     */
    protected $filesRepository;

    protected $ls;

    public function __construct(FilesRepository $filesRepository, LogService $ls)
    {
        $this->filesRepository = $filesRepository;
        $this->ls = $ls;

        //$this->middleware('auth:api')->except(['getNotified', 'show']);

    }

    /** @OA\Get(
     *      path="/filess",
     *      operationId="Files list",
     *      tags={"Files"},
     *       security={{"JWT":{}}},
     *      summary="Return files data",
     *      description="Get all filess",
     *
     *      @OA\Parameter(
     *          name="name",
     *          in="query",
     *          description="Can be used for filtering data by name",
     *          required=false,
     *
     *          @OA\Schema(
     *              type="string"
     *          )
     *      ),
     *
     * @OA\Parameter(
     *          name="project_id",
     *          in="query",
     *          description="Project ID",
     *
     *          @OA\Schema(
     *              type="integer"
     *          )
     *      ),
     *
     * @OA\Parameter(
     *          name="role",
     *          in="query",
     *          description="Files Role ID",
     *          required=false,
     *
     *          @OA\Schema(
     *              type="string"
     *          )
     *      ),
     *
     *      @OA\Parameter(
     *          name="categorie",
     *          in="query",
     *          description="Can be used for filtering data by categorie| ANIMATRICE,RESPONSABLE",
     *          required=false,
     *
     *          @OA\Schema(
     *              type="string"
     *          )
     *          ),
     *
     *      @OA\Response(
     *          response=200,
     *          description="Successful operation",
     *
     *          @OA\JsonContent(ref="#/components/schemas/Files"),
     *
